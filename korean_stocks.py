# 역방향 맵: 티커 → 한글 종목명 (가장 먼저 나오는 이름 사용)
_TICKER_TO_NAME: dict[str, str] = {}
for _name, _ticker in KOREAN_STOCKS.items():
    if _ticker not in _TICKER_TO_NAME:
        _TICKER_TO_NAME[_ticker] = _name

# 6자리 종목코드 → 거래소 접미사 매핑
CODE_TO_EXCHANGE: dict[str, str] = {
    ticker.split(".")[0]: ticker.split(".")[1]
    for ticker in KOREAN_STOCKS.values()
}


def get_display_name(ticker: str) -> str:
    """티커에 대응하는 한글 종목명 반환 (없으면 티커 그대로)."""
    return _TICKER_TO_NAME.get(ticker, ticker)


def search_by_name(query: str) -> str | None:
    """종목명 또는 영문 별칭으로 티커 검색. 없으면 None 반환."""
    q = query.strip().lower()
    if q in KOREAN_STOCKS:
        return KOREAN_STOCKS[q]
    for name, ticker in KOREAN_STOCKS.items():
        if name.lower() == q:
            return ticker
    return None


